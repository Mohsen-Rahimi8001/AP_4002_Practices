class Calculator:
	"""Simple calculator"""

	def add(self, num1:float, num2:float) -> float:
		"""add two numbers"""
		return num1 + num2
	
	def subtract(self, num1:float, num2:float) -> float:
		"""num1 minus num2"""
		return num1 - num2
	
	def multiply(self, num1:float, num2:float) -> float:
		"""multiply two numbers"""
		return num1 * num2
		
	def divide(self, num1:float, num2:float) -> float:
		"""num1 divide by num2"""
		return num1 / num2
